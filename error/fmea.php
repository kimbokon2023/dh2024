<h2 data-ke-size="size26"><b><span style="color: #002060;"><span>FMEA (Failure Mode and Effect Analysis) 무엇인가?</span></span></b></h2>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span style="color: #002060;"><span>장애 모드 및 영향 분석</span><span>(Failure Mode and Effect Analysis)<span>&nbsp;</span></span><span>정의</span></span></b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>제품 또는 프로세스의 잠재적인 장애 모드<span>(failure modes)</span>와 그 영향<span>(the effect of a failure mode)</span>을 체계적으로 분석하는 기법</li>
<li>장애 모드<span>(Failure Mode):<span>&nbsp;</span></span>컴포넌트<span>,<span>&nbsp;</span></span>하위 부품<span>(subassembly),<span>&nbsp;</span></span>제품<span>,<span>&nbsp;</span></span>입력물<span>,<span>&nbsp;</span></span>프로세스<span>,<span>&nbsp;</span></span>서브 프로세스 등이 의도한 일을 제대로 수행하지 못하는 상황<span>.<span>&nbsp;</span></span>이전 오퍼레이션의 결과로 나타날 수도 있고 다음 오퍼레이션을 실패하도록 만들 수도 있다<span>.</span></li>
<li>신규 제품<span>/</span>프로세스를 구현하거나 변경을 가하기 전에 수행하고 그에 따른 적절한 조치를 취하여 발생 가능한 문제를 사전에 막는 예방 목적의 활동<span>(a preventive action)</span>이다<span>.</span></li>
<li>제품 설계 내지는 프로세스 개발 단계에서 수행되는 것이 바람직하지만 기존 제품<span>/</span>프로세스에 적용하는 것도 도움이 될 수 있다<span>.</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span style="color: #002060;">FMEA<span>&nbsp;</span></span></b><b><span style="color: #002060;"><span>발전 과정</span><span><span>(History)</span></span></span></b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li><span>1960</span>년대 중반 항공우주산업 분야에서 안전성<span>(safety)</span>을 위해 처음으로 공식적인<span>&nbsp;</span><span>FMEA<span>&nbsp;</span></span>분석을 수행하였고<span>,<span>&nbsp;</span></span>이후 화학 프로세스 분야에서 안전성 개선을 위한 주요 도구로 부상</li>
<li><span>1974</span>년 미 해군의<span>&nbsp;</span><span>MIL-STD-1629<span>&nbsp;</span></span>표준에서<span><span>&nbsp;</span>FMEA</span>의 사용을 권고</li>
<li><span>1970~1980</span>년대에 원자력<span>,<span>&nbsp;</span></span>항공기<span>,<span>&nbsp;</span></span>화학<span>,<span>&nbsp;</span></span>전자<span>,<span>&nbsp;</span></span>식품 가공 산업 등 여러 분야에서 사용됨</li>
<li>미국 자동차 산업의<span>&nbsp;</span><span>QS-9000<span>&nbsp;</span></span>표준에서 제품 및 프로세스<span>&nbsp;</span><span>FMEA</span>를 공급업자들<span>(suppliers)</span>이 수행할 것을 요구함</li>
<li>최근 의료 산업 분야에서 장애에 따른 위험 평가 및 프로세스 개선 목적으로 적용<span>.<span>&nbsp;</span></span>의료 시설<span>(healthcare facilities)<span>&nbsp;</span></span>관련 표준인<span>&nbsp;</span><span>JCAHO(The Joint Commission for the Accreditation of Healthcare Organizations</span><span>)</span>에서도<span><span>&nbsp;</span>FMEA</span>를 권장</li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span style="color: #002060;">FMEA<span>&nbsp;</span></span></b><b><span style="color: #002060;"><span>타입</span><span><span>(Types of FMEAs)</span></span></span></b></p>
<p data-ke-size="size16">크게 두 가지 유형의<span><span>&nbsp;</span>FMEA</span>가 수행된다</p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>제품<span>/</span>설계<span><span>&nbsp;</span>FMEA(Product/Design FMEAs):<span>&nbsp;</span></span>안전성 위협<span>,<span>&nbsp;</span></span>제품 오작동<span>,<span>&nbsp;</span></span>제품 생명주기 단축 등을 초래하는 제품의 문제를 찾아내는 것이 목표<span>.<span>&nbsp;</span></span>생산에 들어가기 전에 제품 기능에 중점을 두어 제품 설계를 분석하거나 또는 개발 주기의 초기 분석 및 설계 단계에서 시스템과 서브시스템을 분석</li>
<li>프로세스<span><span>&nbsp;</span>FMEA(Process FMEAs):<span>&nbsp;</span></span>제품 제조와 관련된 문제를 찾아내는 것이 목표<span>.<span>&nbsp;</span></span>제조 및 조립 프로세스<span>(manufacturing and assembly processes)</span>를 분석한다<span>.&nbsp;</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span style="color: #002060;">FMEA<span>&nbsp;</span></span></b><b><span style="color: #002060;"><span>적용 절차</span><span><span>(FMEA Procedure)</span></span></span></b></p>
<p data-ke-size="size16"><b><span>1</span>단계<span>: FMEA<span>&nbsp;</span></span>분석 대상 선정</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li><span>FMEA<span>&nbsp;</span></span>분석을 수행할 프로세스나 제품 컴포넌트를 선정하고 적절한<span>(</span>지나치게 크거나 복잡하지 않은<span>)<span>&nbsp;</span></span>범위를 정한다<span>.&nbsp;</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>2</span>단계<span>: FMEA<span>&nbsp;</span></span>팀 조직</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li><span>FMEA</span>는 팀 기반의 분석 기법이므로 내부 조직원이든 외부 인원이든 상관없이 관련된 여러 분야의 전문가<span>(</span>설계자<span>,<span>&nbsp;</span></span>프로세스 전문가<span>,<span>&nbsp;</span></span>재료 공급업자<span>,<span>&nbsp;</span></span>고객<span>/</span>사용자<span>,<span>&nbsp;</span></span>운영자<span>,<span>&nbsp;</span></span>부품 공급자<span>,<span>&nbsp;</span></span>품질 담당자 등<span>)</span>들로 팀을 구성한다<span>.</span></li>
<li>분석 대상의 규모나 성격에 따라 달라지지만 대개<span>&nbsp;</span><span>4~6</span>명 또는<span>&nbsp;</span><span>6~10</span>명의 팀원 구성이 적절하다<span>.</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>3</span>단계<span>: FMEA<span>&nbsp;</span></span>팀 구성원들의 분석 대상 검토</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>미팅을 통해<span><span>&nbsp;</span>FMEA<span>&nbsp;</span></span>팀 구성원들이 만나 분석 대상을 검토한다<span>.</span></li>
<li>프로세스<span><span>&nbsp;</span>FMEA</span>의 경우 플로우차트 등을 활용하여 대상 프로세스의 모든 단계<span>(steps)</span>를 최대한 상세히 식별하고 팀 구성원들의 의견 일치를 본다<span>.&nbsp;</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>4</span>단계<span>:<span>&nbsp;</span></span>장애 모드와 그 원인 및 영향 식별</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>브레인스토밍을 통해 잠재적인 장애 모드<span>(Mode of Failure)</span>를 식별하고 각 장애 모드의 원인<span>(Cause of Failure)<span>&nbsp;</span></span>및 장애가 고객<span>,<span>&nbsp;</span></span>제품<span>,<span>&nbsp;</span></span>프로세스에 미치는 영향<span>(Effect of Failure)</span>을 분석한다<span>.</span></li>
<li><span>FMEA<span>&nbsp;</span></span>분석 결과를 기록하는 작업장인 아래 표와 같은 스프레드시트를 생성하여 식별된 내용을 해당 열에 기록한다<span>.</span></li>
<li>장애 모드와 그 영향<span>(effects)</span>의 관계가 항상<span>&nbsp;</span><span>1<span>&nbsp;</span></span>대<span><span>&nbsp;</span>1</span>인 것은 아니다<span>.</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>5</span>단계<span>:<span>&nbsp;</span></span>각 장애 모드에 대해 발생빈도<span>,<span>&nbsp;</span></span>심각도<span>,<span>&nbsp;</span></span>발견가능성 수치화</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>분석팀은 특정 점수 체계에 따라 각 장애 모드의 발생 빈도<span>(Frequency of Occurrence),<span>&nbsp;</span></span>심각도<span>(Severity of the Failure),<span>&nbsp;</span></span>발견 가능성<span>(Chance of Detection)</span>의 점수를 매긴다<span>.</span></li>
<li>점수 체계<span>(Rating Scales)</span>는<span><span>&nbsp;</span>1~10<span>&nbsp;</span></span>등급 체계가 주로 사용되며<span>&nbsp;</span><span>1~5<span>&nbsp;</span></span>등급 체계도 가능하다<span>. 1~5<span>&nbsp;</span></span>등급 체계는 분석 팀이 점수를 매기기가 좀 더 수월하고<span>, 1~10<span>&nbsp;</span></span>체계는 더 정확한 추정을 가능하게 해준다<span>.</span></li>
</ul>
<p data-ke-size="size16"><b><span>&nbsp;</span></b></p>
<p data-ke-size="size16"><span>&lt;</span>점수 체계 예<span>&gt;</span></p>
<p data-ke-size="size16">발생빈도<span>(1~10): 1=</span>발생 가능성 매우 낮음<span>, 10=</span>발생 가능성 매우 높음</p>
<p data-ke-size="size16">심각도<span>(1~10): 1=</span>피해를 입을 가능성 매우 낮음<span>, 10=</span>심각한 피해를 입을 가능성 매우 높음</p>
<p data-ke-size="size16">발견가능성<span>(1~10): 1=</span>발견 가능성 매우 높음<span>, 10=</span>발견 가능성 매우 낮음</p>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>6</span>단계<span>: RPN(Risk Priority Number)<span>&nbsp;</span></span>계산</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>발생 빈도<span>,<span>&nbsp;</span></span>심각도<span>,<span>&nbsp;</span></span>발견 가능성을 곱하여 각 장애 모드의 리스크 우선 순위<span>(Risk Priority Number: RPN)</span>를 계산한다<span>.</span></li>
<li><span>RPN<span>&nbsp;</span></span>점수는<span><span>&nbsp;</span>1~1000</span>사이가 된다<span>.</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>7</span>단계<span>: RPN</span>을 이용한 개선 노력 계획</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li><span>RPN<span>&nbsp;</span></span>점수가 높은 장애 모드가 시스템<span>/</span>프로세스의 가장 중요한 부분일 가능성이 높으므로 개선 노력이 집중되어야 할 부분이 어딘지를 알려준다<span>.<span>&nbsp;</span></span>예를 들어<span>, RPN<span>&nbsp;</span></span>점수가 가장 높은<span><span>&nbsp;</span>10</span>개의 장애 모드를 선택하여 우선적으로 개선을 위한 노력을 한다<span>.</span></li>
<li><span>RPN<span>&nbsp;</span></span>점수가 높은 중요<span>(</span>고위험<span>)<span>&nbsp;</span></span>장애 모드에 대해 이를 제거하거나 줄일 수 있는 예방책<span>(a remedy<span>&nbsp;</span></span>또는<span>&nbsp;</span><span>an action plan)</span>을 설계한다<span>.<span>&nbsp;</span></span>장애를 완전히 제거하는 것이 불가능한 경우 발생 빈도를 줄이거나<span>,<span>&nbsp;</span></span>심각도를 낮추거나<span>,<span>&nbsp;</span></span>발견 가능성을 높이거나 하는 전략을 취할 수도 있다<span>.<span>&nbsp;</span></span>설계된 예방책이 제대로 적용되고 있는지 확인<span>(validation)</span>할 수 있는 방법도 설계한다<span>.<span>&nbsp;</span></span>아래 워크시트 예에서 보이는 것처럼<span>&nbsp;</span><span>RPN<span>&nbsp;</span></span>점수가 높은 고위험 장애 모드에 대해서만 이런 정보가 기입되어 있다<span>.</span></li>
</ul>
<p data-ke-size="size16">&nbsp;</p>
<p data-ke-size="size16"><b><span>8</span>단계<span>:<span>&nbsp;</span></span>예방 행동을 취하고 재평가</b></p>
<ul style="list-style-type: disc;" data-ke-list-type="disc">
<li>예방책 적용에 따라 고위험 장애 모드가 제거 내지는 감소된 상태에서의 심각도<span>,<span>&nbsp;</span></span>발생 빈도<span>,<span>&nbsp;</span></span>발견 가능성 점수를 재평가하여<span>&nbsp;</span><span>RPN</span>을 다시 계산한다<span>.<span>&nbsp;</span></span>이렇게 새롭게 계산된<span><span>&nbsp;</span>RPN</span>을<span>&nbsp;</span><span>&lsquo;the Resulting RPN&rsquo;</span>이라 지칭한다<span>.</span></li>
<li>장애 모드에 대해<span>&nbsp;</span><span>the Resulting RPN</span>이 만족할만한 수준으로 낮아질 때까지 개선 노력과 예방책 설계 및 적용을 계속한다<span>.</span></li>
</ul>
